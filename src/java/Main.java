package java;

public class Main {
    public static void main(String[] args) {
        System.out.println("Hello World!");
        for (int i = 0; i <= 10; i++) {
            System.out.print(i);
        }
        System.out.println(" ");
        int a = 0;
        while (a <= 10) {
            System.out.print(a);
            a++;
        }
        System.out.println(" ");
        int c = 0;
        do {
            System.out.print(c);
            c++;
        } while (c <= 10);

    }
}
